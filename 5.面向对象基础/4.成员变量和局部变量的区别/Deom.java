
/*
局部变量和成员变量

1.定义的为止不一样【重点】
局部变量：在方法内部
成员变量：字啊方法的外部，直接写在类当中

2.作用范围不一样【重点】
局部变量：只有方法当中可以用，出了方法就不能用了
成员变量：整个类都可以用

3.默认值不一样【重点】
局部变量：没有默认值，如果想用，必须先赋值
成员变量：如果没有赋值，则有默认值，规则和数组一样

4.内存的位置不一样
局部变量：位于栈内存
成员变量：位于堆内存

5.生命周期不一样
局部变量：随着方法的进栈而诞生，随着方法出栈而消失
成员变量：随着对象的创建而产生，随着对象被垃圾回收而消失

*/

